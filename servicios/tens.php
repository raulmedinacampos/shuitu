		<p class="text-justify">Como su nombre lo indica, la electro
			estimulación nerviosa trans cutánea (Tens), es una máquina que alivia
			el dolor rápidamente en la aplicación prolongada sobre pacientes con
			algias (lumbalgia, cervicalgia, etc.) en cualquier parte del cuerpo
			relacionadas a efectos traumáticos o nerviosos, ya que viaja a través
			de las fibras nerviosas de tipo A (gruesas), C (delgadas) y T
			(uniones de transmisión de fibras nerviosas) por medio de electrodos,
			esto gracias a que el Tens puede aumentar la producción de endorfinas
			con la estimulación eléctrica.</p>
